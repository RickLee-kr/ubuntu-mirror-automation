  mkdir -p "$root/etc/update-manager/release-upgrades.d" \
    "$root/opt/aelladata/os-upgrade/offline"
  printf '# stellar offline DistUpgrade ValidMirrors overlay (Xenial-to-Bionic)\nhttp://archive.ubuntu.com/ubuntu/\nhttp://example.invalid/hops/xenial-to-bionic/ubuntu\nhttp://example.invalid/hops/xenial-to-bionic/ubuntu/\n' \
    >"$root/opt/aelladata/os-upgrade/offline/distupgrade-valid-mirrors.cfg"
  printf '# Generated by stellar offline Xenial-to-Bionic client.\n[Sources]\nValidMirrors=%s\nAllowThirdParty=yes\n' \
    "$root/opt/aelladata/os-upgrade/offline/distupgrade-valid-mirrors.cfg" \
    >"$root/etc/update-manager/release-upgrades.d/99-stellar-offline-mirror.cfg"
}

