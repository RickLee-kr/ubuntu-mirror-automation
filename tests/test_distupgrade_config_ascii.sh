# Helper to inject bad byte sequence into override cfg
inject_cfg() {
  local root="$1"
  local label="$2"
  local raw_hex="$3"
  setup_ascii_root "$root"
  python3 - "$root" "$raw_hex" <<'PY'
import sys, binascii
root, hx = sys.argv[1], sys.argv[2]
path = root + "/etc/update-manager/release-upgrades.d/99-stellar-offline-mirror.cfg"
seq = binascii.unhexlify(hx.replace(" ", ""))
open(path, "wb").write(b"# bad " + seq + b" comment\n[Sources]\nAllowThirdParty=yes\n")
PY
}

expect_nonascii() {
  local root="$1"
  local label="$2"
  set +e
  run_gate "$root"
  rc=$?
  set -e
  if [[ "$rc" -ne 0 ]] && grep -q 'FAIL_DISTUPGRADE_CONFIG_NONASCII' "$root/gate.err"; then
    pass "$label -> FAIL_DISTUPGRADE_CONFIG_NONASCII"
  else
    fail "$label expected NONASCII fail"
    cat "$root/gate.err" || true
  fi
}

