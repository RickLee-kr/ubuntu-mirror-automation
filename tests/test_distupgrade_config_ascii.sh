# DistUpgradeConfigParser ASCII-only config gate (Xenial→Bionic).
# Reproduces Bionic UpgradeTool UnicodeDecodeError under LC_ALL=C and verifies
# client fail-closed pre-DRO validation. No real do-release-upgrade transaction.
