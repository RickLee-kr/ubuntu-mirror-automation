# Python 3.12 defaults to UTF-8 mode; force locale encoding like Xenial 3.5.
run_distupgrade_config() {
  local ov="$1"
  ( cd "$PARSER_DIR" && \
    LC_ALL=C LANG=C PYTHONUTF8=0 python3 - "$ov" <<'PY'
from DistUpgradeConfigParser import DistUpgradeConfig
import sys
c = DistUpgradeConfig(".", override_dir=sys.argv[1])
print("CONFIG_FILES=" + ",".join(c.config_files))
if c.has_option("Sources", "AllowThirdParty"):
    print("ALLOW=" + c.get("Sources", "AllowThirdParty"))
print("DISTUPGRADE_CONFIG_INIT=PASS")
PY
  )
}

