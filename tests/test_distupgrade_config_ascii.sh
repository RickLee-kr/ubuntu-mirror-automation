run_gate() {
  local root="$1"
  env -i PATH="/usr/bin:/bin" HOME=/tmp \
    STELLAR_OFFLINE_TEST_ROOT="$root" \
    /bin/bash -c "source '$GATE_SH'; validate_distupgrade_config_ascii_before_dro" 2>"$root/gate.err"
}

setup_ascii_root() {
